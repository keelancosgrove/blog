	<h2 class="article_title"> Winter Break 2015-2016 </h2>
	<p class="article_text">During the 2015-2016 winter break (which was over a month long), I travelled to Colorado and Utah. 
		I haven't been out west much, nor have I done much travelling individually. 
		Staying in hostels and using public transportation wasn't much different from what I expected.
		 I stayed in Denver and Salt Lake City, each about 5 days. In Denver, I walked/rented a bike and explored the city,
		  including the Museum of Nature and Science, Botanical Gardens, Cherry Creek, Downtown, and more.
		   I also made day trips out to Boulder and hiked the nearby mountains. I spent nearly an entire day in the Chautauqua park, 
		   as well as part of another day in Mount Sanitas. In Salt Lake City, I explored the city but was primarily there for snowboarding.
		    I spent a day at Snowbird and three days at Park City. Snowbird was a challenging mountain
		     - even the easier trails include narrow passes bordered by cliffs, and it's important to maintain at least a moderate amount of speed 
		     to avoid stopping on some of the flatter terrain. Park City was a much "friendlier" resort, but still offered plenty of challenging terrain, 
		     especially through moguls and the bowls.
 	
